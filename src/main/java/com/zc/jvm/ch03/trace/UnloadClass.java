package com.zc.jvm.ch03.trace;

import org.objectweb.asm.ClassWriter;
import org.objectweb.asm.MethodVisitor;
import org.objectweb.asm.Opcodes;

import java.lang.reflect.InvocationTargetException;
import java.lang.reflect.Method;

/**
 * 使用 ASM 动态生成 名为 Example 的类,并将其反复加载到系统中,然后 手动触发 fullgc,跟踪类的加载/卸载
 * 1.分别使用 -XX:+TraceClassUnloading 跟踪类的卸载, -XX:+TraceClassLoading 跟踪类的加载
 * -XX:+TraceClassUnloading -XX:+TraceClassLoading
 *
 * 2.直接使用 -verbose:class 跟踪类的加载/卸载
 * -verbose:class
 */
public class UnloadClass implements Opcodes {
    public static void main(String args[]) throws NoSuchMethodException, SecurityException, IllegalAccessException, IllegalArgumentException, InvocationTargetException {
        ClassWriter cw = new ClassWriter(ClassWriter.COMPUTE_MAXS | ClassWriter.COMPUTE_FRAMES);
        cw.visit(V1_7, ACC_PUBLIC, "Example", null, "java/lang/Object", null);
        MethodVisitor mw = cw.visitMethod(ACC_PUBLIC, "<init>", "()V", null, null);
        mw.visitVarInsn(ALOAD, 0);
        mw.visitMethodInsn(INVOKESPECIAL, "java/lang/Object", "<init>", "()V");
        mw.visitInsn(RETURN);
        mw.visitMaxs(0, 0);
        mw.visitEnd();
        mw = cw.visitMethod(ACC_PUBLIC + ACC_STATIC, "main", "([Ljava/lang/String;)V", null, null);
        mw.visitFieldInsn(GETSTATIC, "java/lang/System", "out", "Ljava/io/PrintStream;");
        mw.visitLdcInsn("Hello world!");
        mw.visitMethodInsn(INVOKEVIRTUAL, "java/io/PrintStream", "println", "(Ljava/lang/String;)V");
        mw.visitInsn(RETURN);
        mw.visitMaxs(0, 0);
        mw.visitEnd();
        byte[] code = cw.toByteArray(); // ASM 生成名为 Example 的类,保存到 code 中

        for (int i = 0; i < 10; i++) {
            // UnloadClassLoader 将新生成的类反复加载到系统中,每次循环使用新的 ClassLoader 实例,在循环结束前进行 Full GC,释放上一次循环加载的类.因此,这一过程会涉及类的加载和卸载.
            UnloadClassLoader loader = new UnloadClassLoader();
            Method m = ClassLoader.class.getDeclaredMethod("defineClass", String.class, byte[].class, int.class, int.class);
            m.setAccessible(true);
            m.invoke(loader, "Example", code, 0, code.length);
            m.setAccessible(false);
            System.gc(); // full gc
        }
    }
}
